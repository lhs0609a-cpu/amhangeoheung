/// 리뷰 가이드 클래스
class ReviewGuide {
  final String category;
  final List<String> tips;
  final List<String> prosExamples;
  final List<String> consExamples;
  final String reviewTemplate;
  final Map<String, String> scoreCriteria;

  const ReviewGuide({
    required this.category,
    required this.tips,
    required this.prosExamples,
    required this.consExamples,
    required this.reviewTemplate,
    required this.scoreCriteria,
  });
}

